@Composable
fun DotMatrixText(
    text: String,
    modifier: Modifier = Modifier,
    activeColor: Color = Color.White,
    inactiveColor: Color = Color(0x15FFFFFF), // Ultra faint white dots for matrix display feel
    dotSize: Dp = 4.dp,
    spacing: Dp = 2.dp,
    charSpacing: Dp = 6.dp,
    showBgGrid: Boolean = true
) {
    val density = LocalDensity.current
    
    // Convert Dps to Px inside DrawScope or pre-calculate
    val dotSizePx = with(density) { dotSize.toPx() }
    val spacingPx = with(density) { spacing.toPx() }
    val charSpacingPx = with(density) { charSpacing.toPx() }

    val charWidthPx = 5 * dotSizePx + 4 * spacingPx
    val charHeightPx = 7 * dotSizePx + 6 * spacingPx

    val textLength = text.length
    val totalWidthPx = textLength * charWidthPx + if (textLength > 1) (textLength - 1) * charSpacingPx else 0f

    val totalWidthDp = with(density) { totalWidthPx.toDp() }
    val totalHeightDp = with(density) { charHeightPx.toDp() }

    Canvas(
        modifier = modifier
            .width(totalWidthDp)
            .height(totalHeightDp)
    ) {
        val radius = dotSizePx / 2f

        for (charIndex in 0 until textLength) {
            val char = text[charIndex]
            val glyph = DotMatrixFont.get(char)
            
            val charColOffset = charIndex * (charWidthPx + charSpacingPx)

            for (row in 0 until 7) {
                // Get the row bits: glyph is an IntArray of size 7. 
                // Each element represents a row of 5 bits (0x00 - 0x1F).
                val rowVal = if (row < glyph.size) glyph[row] else 0
                
                for (col in 0 until 5) {
                    // Inspect 5-bit columns. MSB is col 0, LSB is col 4.
                    // To extract, check bit at position (4 - col)
                    val bitShift = 4 - col
                    val isBitSet = ((rowVal shr bitShift) and 1) == 1

                    val centerX = charColOffset + col * (dotSizePx + spacingPx) + radius
                    val centerY = row * (dotSizePx + spacingPx) + radius

                    if (isBitSet) {
                        drawCircle(
                            color = activeColor,
                            radius = radius,
                            center = Offset(centerX, centerY)
                        )
                    } else if (showBgGrid) {
                        drawCircle(
                            color = inactiveColor,
                            radius = radius,
                            center = Offset(centerX, centerY)
                        )
                    }
                }
            }
        }
    }
}
